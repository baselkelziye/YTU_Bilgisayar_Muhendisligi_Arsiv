STSG SEGMENT PARA STACK 'STSGM'
	DW 20 DUP (?)
STSG ENDS

DTSG SEGMENT PARA 'DTSGM'
	ASAY DW 0F000H
	BSAY DW 0E000H
	SONUC DD 0
DTSG ENDS

CDSG SEGMENT PARA 'CDSGM'
	ASSUME CS: CDSG, DS: DTSG, SS: STSG
	
	ANA PROC FAR

		PUSH DS
		XOR AX, AX
		PUSH AX

		MOV AX, DTSG
		MOV DS, AX

		LEA BX, SONUC
		MOV AX, ASAY
		ADD AX, BSAY
		MOV [BX], AX
		JNC L1
		INC WORD PTR [BX+2]
L1:		
		RETF
	ANA ENDP


CDSG ENDS

	END ANA