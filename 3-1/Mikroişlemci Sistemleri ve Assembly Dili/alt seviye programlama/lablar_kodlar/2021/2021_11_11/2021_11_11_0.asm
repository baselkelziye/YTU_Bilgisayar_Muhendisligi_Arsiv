	SON PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX

		MOV AX, DATASG
		MOV DS, AX
		XOR DX, DX
		XOR CX, CX
		MOV AX, 1
		MOV CL, B
		XOR BX, BX
		MOV BL, A
		L1:
		MUL BX
		LOOP L1
		MOV SONUC, AX
		RETF
	SON ENDP
CODESG ENDS
	END SON