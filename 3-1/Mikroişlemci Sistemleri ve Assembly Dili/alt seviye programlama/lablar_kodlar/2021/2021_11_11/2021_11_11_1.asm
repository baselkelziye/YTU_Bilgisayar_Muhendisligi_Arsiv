DTSG SEGMENT WORD 'DTK'
	ASAY DW 0F000H
	BSAY DW 0E000H
	SONUC DD 0
DTSG ENDS

CDSG SEGMENT WORD 'CDK'
	ASSUME CS: CDSG, DS: DTSG, SS: STSG

	ANA PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX

		MOV AX, DTSG
		MOV DS, AX

		LEA BX, SONUC

		MOV AX, ASAY
		ADD AX, BSAY

		MOV [BX], AX
		ADC WORD PTR [BX+2], 0


		RETF
	ANA ENDP

CDSG ENDS

	END ANA